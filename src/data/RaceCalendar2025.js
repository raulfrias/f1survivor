/**
 * Get the next upcoming race from the static calendar
 * @param {Date} currentDate - Current date to compare against
 * @returns {Object|null} Next race object or null if no future races
 */
export function getNextRace(currentDate = new Date()) {
  const futureRaces = F1_2025_CALENDAR.filter(race => 
    new Date(race.dateStart) > currentDate
  );
  
  if (futureRaces.length === 0) {
    return null;
  }
  
  // Sort by date and return the earliest upcoming race
  return futureRaces.sort((a, b) => 
    new Date(a.dateStart) - new Date(b.dateStart)
  )[0];
}

/**
 * Get race by ID
 * @param {string} raceId - Race ID to find
 * @returns {Object|null} Race object or null if not found
 */
export function getRaceById(raceId) {
  return F1_2025_CALENDAR.find(race => race.id === raceId) || null;
}

/**
 * Get all races for a given month
 * @param {number} year - Year (2025)
 * @param {number} month - Month (1-12)
 * @returns {Array} Array of race objects for that month
 */
export function getRacesByMonth(year = 2025, month) {
  return F1_2025_CALENDAR.filter(race => {
    const raceDate = new Date(race.dateStart);
    return raceDate.getFullYear() === year && raceDate.getMonth() === month - 1;
  });
} 